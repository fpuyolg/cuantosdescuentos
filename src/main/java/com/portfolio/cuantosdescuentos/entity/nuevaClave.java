package com.portfolio.cuantosdescuentos.entity;

public class nuevaClave {
	String dni;
	String claveActual;
	String nuevaClave;
	String nuevaClaveRep;
	
	
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getClaveActual() {
		return claveActual;
	}
	public void setClaveActual(String claveActual) {
		this.claveActual = claveActual;
	}
	public String getNuevaClave() {
		return nuevaClave;
	}
	public void setNuevaClave(String nuevaClave) {
		this.nuevaClave = nuevaClave;
	}
	public String getNuevaClaveRep() {
		return nuevaClaveRep;
	}
	public void setNuevaClaveRep(String nuevaClaveRep) {
		this.nuevaClaveRep = nuevaClaveRep;
	}
	
	
}
